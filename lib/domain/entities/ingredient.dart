class Ingredient {

  String aisle;
  double amount;
  int id;
  String image;
  String name;
  String original;
  String originalName;
  String unit;
  String unitLong;
  String unitShort;

  Ingredient({
    required this.aisle,
    required this.amount,
    required this.id,
    required this.image,
    required this.name,
    required this.original,
    required this.originalName,
    required this.unit,
    required this.unitLong,
    required this.unitShort,
  });
}
